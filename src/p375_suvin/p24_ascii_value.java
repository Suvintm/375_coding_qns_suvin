//24) Write a program to find the ASCII value of a character.

package p375_suvin;

public class p24_ascii_value {
    public static void main(String[] args) {
        char ch = 'A';
        int ascii = (int) ch;
        System.out.println("ASCII value of " + ch + " is: " + ascii);
    }
}

//output
//ASCII value of A is: 65
