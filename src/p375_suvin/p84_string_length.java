//84) Write a program to find the length of a string.

package p375_suvin;

public class p84_string_length {
    public static void main(String[] args) {
        String str = "Hello Java";
        int len = str.length();
        System.out.println("String: " + str);
        System.out.println("Length: " + len);
    }
}

//output
//String: Hello Java
//Length: 10
