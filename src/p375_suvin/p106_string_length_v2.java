//106) Write a program to find the length of a string.

package p375_suvin;

public class p106_string_length_v2 {
    public static void main(String[] args) {
        String s = "Advanced Java";
        System.out.println("String: " + s);
        System.out.println("Length: " + s.length());
    }
}

//output
//String: Advanced Java
//Length: 13
