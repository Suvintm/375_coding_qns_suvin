//77) Write a program to perform arithmetic operations using float and double data types.

package p375_suvin;

public class p77_float_double_ops {
    public static void main(String[] args) {
        float f1 = 10.5f, f2 = 2.5f;
        double d1 = 20.4, d2 = 4.0;
        
        System.out.println("Float Sum: " + (f1 + f2));
        System.out.println("Float Div: " + (f1 / f2));
        System.out.println("Double Sum: " + (d1 + d2));
        System.out.println("Double Div: " + (d1 / d2));
    }
}

//output
//Float Sum: 13.0
//Float Div: 4.2
//Double Sum: 24.4
//Double Div: 5.1
