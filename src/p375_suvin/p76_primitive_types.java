//76) Write a program to demonstrate the use of primitive data types in Java.

package p375_suvin;

public class p76_primitive_types {
    public static void main(String[] args) {
        byte b = 10;
        short s = 20;
        int i = 30;
        long l = 40L;
        float f = 50.5f;
        double d = 60.6;
        char c = 'A';
        boolean bool = true;
        
        System.out.println("byte: " + b);
        System.out.println("short: " + s);
        System.out.println("int: " + i);
        System.out.println("long: " + l);
        System.out.println("float: " + f);
        System.out.println("double: " + d);
        System.out.println("char: " + c);
        System.out.println("boolean: " + bool);
    }
}

//output
//byte: 10
//short: 20
//int: 30
//long: 40
//float: 50.5
//double: 60.6
//char: A
//boolean: true
